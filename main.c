int main(void) 
{
	bool exit = false;
    int* dynamicArray;
    int n;
    
    while (!exit)
	{
		
		printMenu();
		
        char input;
        printf("\n");
        printf("Input: "); 
		scanf(" %c", &input);

        switch (input)
        {
	        case 'e':
	            exitProgram(&dynamicArray, &exit);
	            break;
	        case 'c':
	            createArray(&dynamicArray, &n);
	            break;
	        case 'p':
	            printArray(&dynamicArray, &n);
	            break;
	        case 'i':
	            insert(&dynamicArray, &n);
	            break;
	        case 'd':
	            deleteElement(&dynamicArray, &n);
	            break;
	        case 's':
	            increSort(&dynamicArray, &n);
	            break;
	        case 'x':
	            decreSort(&dynamicArray, &n);
	            break;
	        case 't':
	            search(&dynamicArray, &n);
	            break;
	        
	        default:
	            printf("Invalid input. Try again\n");
	            Sleep(1000);
        }
        
        
        system("cls");
    }
    
	return 0;
}

